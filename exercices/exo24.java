package exercices;

 /*  EXERCICE 4 : Faire le compte à rebour à partir de 10, affichez successivment le décompte, une fois à 0 affichez bonne année ! */
 
public class exo24 {

    public static void main(String[] args) {
        for(int i = 10; i>=0; i--){
            System.out.println(i);
        }
        System.out.println("Bonne année !");
    }
}
